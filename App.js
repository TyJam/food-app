 const addNode = (e) =>{
  document.addEventListener('click', (e) =>{
      
    //Baill if it's not a .nav-link
    if(!e.target.classList.contains('food')) return;
      if(e.target.classList.contains('seafood')){
            removeNode(e);
            main.insertAdjacentHTML('afterbegin', seafood);
      }else if(e.target.classList.contains("Gumbo")){
          removeNode(e);
          main.insertAdjacentHTML('afterbegin', Gumbo)
      }else if(e.target.classList.contains('Chicken')){
         removeNode(e);
         main.insertAdjacentHTML('afterbegin', Chicken)
      }else if(e.target.classList.contains("Jambalaya")){
        removeNode(e);
        main.insertAdjacentHTML("afterbegin", Jambalaya)
      }

    //Add the active class 
    event.target.classList.add('active');
          

    //Get all nav links 
    var links = document.querySelectorAll('.food');

     
    //Loop through each link 
    for (var i = 0; i< links.length; i++){

      //if the link is the one clicked skip it 
      if(links[i] === event.target) continue;

      //remove the .active class 
      links[i].classList.remove('active')
    }

  }, false);
 
 }
  
